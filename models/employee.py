
class Employee:
    def __init__(self, employeeId, nationalIdnumber, loginId, organizationLevel, jobTitle, birthDate, maritalStatus,
                 gender, hireDate, isSalaried, vacationHours, sickLeaveHours, isActive, modifiedDate):
        self.employeeId = employeeId
        self.nationalIdnumber = nationalIdnumber
        self.loginId = loginId
        self.organizationLevel = organizationLevel
        self.jobTitle = jobTitle
        self.birthDate = birthDate
        self.maritalStatus = maritalStatus
        self.gender = gender
        self.hireDate = hireDate
        self.isSalaried = isSalaried
        self.vacationHours = vacationHours
        self.sickLeaveHours = sickLeaveHours
        self.isActive = isActive
        self.modifiedDate = modifiedDate
